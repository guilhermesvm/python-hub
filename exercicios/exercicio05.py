# 5 – Exibir para o usuário o valor e o tipo de dado da variável ano_nascimento( Atribuir o seu ano de nascimento).

# A variavel ano_nascimento está sendo convertida para int(inteiro)

ano_nascimento = int(input("Digite o ano que você nasceu: "))

print("Você nasceu no ano: ",ano_nascimento)

# Type - Palavra reservada pelo python para mostrar o tipo de variavel, que neste caso é inteiro.
print("O tipo de variavel é: ", type(ano_nascimento))